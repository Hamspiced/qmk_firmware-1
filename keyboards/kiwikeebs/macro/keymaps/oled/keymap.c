void arasaka_logo_clean(void)
{
    oled_write_raw_P(logo_clean, 512);
}

void arasaka_logo_glitch(void)
{
  uint8_t frame = rand() % 7;

  if (frame < 5) {
    oled_write_raw_P(logo_glitch[frame], 512);

    return;
  }

  oled_write_raw_P(logo_glitch_dirty[frame - 5], 512);
}

void arasaka_logo_glitch_dirty(void)
{
  oled_write_raw_P(logo_glitch_dirty[rand() % 2], 512);
}

void arasaka_draw(void) {
  uint16_t timer = timer_elapsed(arasaka_timer);

  if (timer < 150) {
    show_text ? arasaka_logo_glitch_dirty() : arasaka_logo_glitch();

    return;
  }

  if (timer < 250) {
    show_text ? arasaka_logo_glitch(): arasaka_logo_glitch_dirty();

    return;
  }

  if (timer > 9750 && timer < 9850) {
    show_text ? arasaka_logo_glitch(): arasaka_logo_glitch_dirty();

    return;
  }

  if (timer > 9850 && timer < 10000) {
    show_text ? arasaka_logo_glitch_dirty() : arasaka_logo_glitch();

    return;
  }

  if (timer > 10000) {
    show_text = !show_text;
    arasaka_timer = timer_read();
  }

  if (glitch && 0 != frame_count) {
    frame_count--;
    if (show_text) {
      arasaka_logo_glitch();

      return;
    }

    arasaka_logo_glitch();

    return;
  }

  glitch = false;

  if (show_text) {
    arasaka_logo_clean();
  } else {
    arasaka_logo_clean();
  }

  if (1 == rand() % 50) {
    glitch = true;
    frame_count = 2 + rand() % 10;
  }
}

bool oled_task_user(void) {
  oled_set_brightness(0);
  arasaka_draw();
  oled_on();
  return false;
}
