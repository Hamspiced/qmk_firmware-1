 void change_tab(bool clockwise) {
     if (clockwise){
        // on keydown
            register_code(KC_LALT);
            register_code(KC_LGUI);
            register_code(KC_LEFT);
        // on keyup
            unregister_code(KC_LALT);
            unregister_code(KC_LGUI);
            unregister_code(KC_LEFT);
     }else{
        // on keydown
            register_code(KC_LALT);
            register_code(KC_LGUI);
            register_code(KC_RIGHT);
        // on keyup
            unregister_code(KC_LALT);
            unregister_code(KC_LGUI);
            unregister_code(KC_RIGHT);
            }

     }


bool encoder_update_kb(uint8_t index, bool clockwise) {


    switch(COUNTER){
        case 1:
        if (clockwise) {
            tap_code(KC_VOLD);
        } else {
            tap_code(KC_VOLU);
        }
        break;

        case 2:
        if (clockwise) {
            tap_code(KC_MPRV);
        } else {
            tap_code(KC_MNXT);
        }
        break;

        case 3:
        if (clockwise) {
            tap_code(KC_BRID);
        } else{
            tap_code(KC_BRIU);
        }
        break;

        case 4:

            change_tab(clockwise);

        break;
    }
 return true;
}

