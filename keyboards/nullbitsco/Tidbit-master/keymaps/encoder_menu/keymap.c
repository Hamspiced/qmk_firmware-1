bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    // Send keystrokes to host keyboard, if connected (see readme)
     if (keycode == KC_F16 && record->event.pressed) {
                    if(COUNTER > 4){
                        COUNTER = 1;
                     }
                    else{
                        COUNTER = COUNTER+1;
                    }
     }
        switch(COUNTER){
            case 1:
            oled_write_raw_P(volume_icon,sizeof(volume_icon));
            break;

            case 2:
            oled_write_raw_P(music_note,sizeof(music_note));
            break;

            case 3:
            oled_write_raw_P(LED_icon,sizeof(LED_icon));
            break;

            case 4:
            oled_write_raw_P(light_icon,sizeof(light_icon));
            break;

            case 5:
            oled_write_raw_P(tab_icon,sizeof(tab_icon));
            break;
        }

     if (keycode == KC_F15 && record->event.pressed  && COUNTER == 2) {
            tap_code(KC_MPLY);
            }

       return true;
    }

